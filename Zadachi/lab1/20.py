# Ввод данных от пользователя
kwh = float(input("Введите количество израсходованных КВт·ч за месяц: "))
price_per_kwh = float(input("Введите стоимость 1 КВт·ч (в рублях): "))

# Проверка на отрицательные значения
if kwh < 0 or price_per_kwh < 0:
    print("Ошибка: количество КВт·ч и стоимость не могут быть отрицательными!")
else:
    # Вычисление общей суммы
    total_cost = kwh * price_per_kwh

    # Вывод результата
    print(f"\nРасчет оплаты за электроэнергию:")
    print(f"Израсходовано: {kwh} КВт·ч")
    print(f"Стоимость 1 КВт·ч: {price_per_kwh} руб.")
    print(f"Общая сумма к оплате: {total_cost:.2f} руб.")