# Получаем два числа от пользователя
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))

# Вычисляем кубы чисел
cube1 = num1 ** 3
cube2 = num2 ** 3

# Находим среднее арифметическое кубов
avg_cubes = (cube1 + cube2) / 2

# Находим модули чисел
abs1 = abs(num1)
abs2 = abs(num2)

# Находим среднее геометрическое модулей (квадратный корень от произведения)
geom_abs = (abs1 * abs2) ** 0.5

# Выводим результаты
print(f"Кубы чисел: {cube1} и {cube2}")
print(f"Среднее арифметическое кубов: {avg_cubes}")
print(f"Модули чисел: {abs1} и {abs2}")
print(f"Среднее геометрическое модулей: {geom_abs}")