import math

# Ускорение свободного падения (м/с²)
g = 9.81

# Ввод высоты
h = float(input("Введите высоту падения h (в метрах): "))

# Проверка на неотрицательную высоту
if h < 0:
    print("Высота не может быть отрицательной!")
else:
    # Вычисление времени падения по формуле: t = √(2h/g)
    time = math.sqrt(2 * h / g)

    # Вывод результата
    print(f"Время падения с высоты {h} м: {time:.2f} секунд")