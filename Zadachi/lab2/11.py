# Ввод трехзначного числа
number = int(input("Введите трехзначное число: "))

# Проверяем, что число действительно трехзначное
if 100 <= number <= 999:
    # Разбиваем число на цифры
    digit1 = number // 100  # первая цифра (сотни)
    digit2 = (number // 10) % 10  # вторая цифра (десятки)
    digit3 = number % 10  # третья цифра (единицы)

    # Проверяем, есть ли цифра 5 среди этих трех цифр
    if digit1 == 5 or digit2 == 5 or digit3 == 5:
        print("ДА")
    else:
        print("НЕТ")
else:
    print("Ошибка! Введите трехзначное число.")