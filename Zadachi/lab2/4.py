# Ввод количества материалов
M = int(input("Введите количество ручек (M): "))
F = int(input("Введите количество линеек (F): "))
N = int(input("Введите количество тетрадей (N): "))

# Проверка на отрицательные значения
if M < 0 or F < 0 or N < 0:
    print("Ошибка: количество не может быть отрицательным!")
else:
    # Вычисление максимального количества наборов по каждому компоненту
    sets_by_pens = M // 1  # 1 ручка на набор
    sets_by_rulers = F // 2  # 2 линейки на набор
    sets_by_notebooks = N // 4  # 4 тетради на набор

    # Общее количество наборов ограничено самым дефицитным компонентом
    total_sets = min(sets_by_pens, sets_by_rulers, sets_by_notebooks)

    # Вывод результатов
    print(f"\nРезультаты расчета:")
    print(f"Ручки: {M} шт. → максимум {sets_by_pens} наборов")
    print(f"Линейки: {F} шт. → максимум {sets_by_rulers} наборов")
    print(f"Тетради: {N} шт. → максимум {sets_by_notebooks} наборов")
    print(f"Можно собрать подарочных наборов: {total_sets}")

    # Вывод остатков материалов
    if total_sets > 0:
        print(f"\nОстатки после сборки:")
        print(f"Ручки: {M - total_sets * 1} шт.")
        print(f"Линейки: {F - total_sets * 2} шт.")
        print(f"Тетради: {N - total_sets * 4} шт.")